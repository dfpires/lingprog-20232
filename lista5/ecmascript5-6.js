// função normal sem seta
function bomDia(){
    console.log('Bom dia')
}
bomDia()
// arrow function - função com seta
// função que será uma variável
let bomDia2 = () => {
    console.log('Bom dia')
}
bomDia2()
// arrow function com 2 parâmetros
let somar = (n1,n2) => {
    console.log(n1+n2)
}
somar(5, 9)
